package com.calidad2018.pcc.colaboradores;

public class Colaborador {


    private String name;

    private String id;

    public Colaborador() {
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }
}
